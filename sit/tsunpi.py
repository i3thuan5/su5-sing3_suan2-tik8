from 臺灣言語工具.斷詞.國教院斷詞用戶端 import 國教院斷詞用戶端


class 準備語料:
    def 斷詞(self, 華語句物件):
        斷詞結果 = 國教院斷詞用戶端.語句斷詞做陣列(華語句物件.看型(' ', ' '))
        相仝 = True
        for guan, tng in zip(華語句物件.網出詞物件(), 斷詞結果):
            if guan.看型() != tng[0]:
                相仝 = False
        return {
            '國教院斷詞': 斷詞結果,
            '國教院斷詞kah翻譯有仝無': 相仝,
        }
